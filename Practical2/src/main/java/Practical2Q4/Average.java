package Practical2Q4;

public class Average {
    public double calculateAverage(int number1, int number2) {
        return (number1 + number2) / 2.0;
    }

    public double calculateAverage(int number1, int number2, int number3) {
        return (number1 + number2 + number3) / 3.0;
    }

    public double calculateAverage(double number1, double number2) {
        return (number1 + number2) / 2.0;
    }

    public double calculateAverage(double number1, double number2, double number3) {
        return (number1 + number2 + number3) / 3.0;
    }
}
